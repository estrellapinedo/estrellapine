# Calcular la edad de una persona a partir de su año de nacimiento

# Definir una variable que reciba el numero de año de nacimiento  de la persona 
year_birthday = input("Ingresa el año de tu nacimiento: ")

# Operacion de calculo de edad: año_actual - año_nacimiento
edad = 2025 - int(year_birthday)
# Visualiza la edad
print(f"La edad de la persona es: {edad}")
